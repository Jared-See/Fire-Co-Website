                         Introduction— <br><br>
                         
                         “We  face  the  past  with  pride  and  the  future  with  courage.”  This  motto  frames  the  history  of  the  Ogontz  Fire<br>
                          Company  and  our  hopes  for  the  future.  Prior  to  1892  the  village  of  Ogontz  (the  area  today  comprising  the  <br>
                          Cheltenham  Township  portion  of  Elkins  Park)  was  made  up  of  a  few  estates,  farms,  and  a  collection  of  <br>
                          hamlets—certainly  not  the  teeming  Philadelphia  suburb  it  is  today.  Fire  protection  was  limited  to  ad  hoc  <br>
                          formation  of  bucket  brigades  organized  on  the  spot  whenever  the  need  arose.<br><br>
                          
                         Formation—<br><br>

                          Early  in  1892  Frank  Engle,  owner  of  a  general  store,  and  druggist  Abraham  T.  Clayton  raised  $100  to  install<br>
                          a  fire  hydrant  in  the  town.  In  October  of  that  year  Ogontz  Fire  Company  was  founded  in  response  to  that  <br>
                          installation.  Its  first  officers  were:  Frank  Engle,  president;  Louis  A.  Nagle,  vice  president,  and  A.T.  Clayton,<br>
                          secretary-treasurer.  Mr.  Engle  also  served  as  the  first  chief  of  Ogontz  Fire  Company. <br><br>
                           
                         In  December  1892,  a  drive  for  funds  netted  $175;  of  this,  $160  was  spent  for  250  feet  of  hose  and  the <br>
                         remaining  $15  was  paid  to  Horace  Ervien,  Sr.  for  a  hose  cart.<br><br>
                         
                          A  subsequent  fund  drive  in  1896  netted  an  additional  $125,  which  was  used  to  replace  hose  and  to  pay  Mr.<br>
                          Ervien  $17.50  to  build  a  larger  hose  cart.  The  members  symbolically  buried  the  old  cart.  There  remained  $3<br>
                          on  hand  when  the  year  ended.<br><br>
                          
                          
                          
                          Charter—<br><br>
                          
                          In  1908  the  firemen  (unlike  today’s  crew,  they  were  all  men)  petitioned  to  become  chartered  and  their  <br>
                          application  was  granted  on  September  8th.  Charter  members  included  Frank  Engle;  Abraham  Clayton; <br>
                          Joseph  Bosler,  owner  and  operator  of  a  grist  mill  (who  also  served  as  also  assistant  Treasurer  of  the  United<br>
                          States  at  the  time);  Morris  P.  Horner  (plumber);  Alfred  Tyson,  Carl  J.H.  Luckow  (barber);  Horace  Ervien,<br>
                          and  Eugene  Hawlk,  owner  of  the  Cheltenham  Coal  and  Ice  Company.<br><br>
                               
                          On  September  16th  1908,  the  officers  held  a  meeting  and  voted  every  resident  of  the  village  a  fireman.<br>
                          A  new  and  more  up-to-date  hand  reel,  capable  of  carrying  500  feet  of  hose,  one  nozzle,  two  lanterns,  a  chief’s<br>
                          white  hat,  a  white  rubber  coat,  two  fire  axes  and  a  fire  bell  replaced  the  old  hose  cart.  In  1909,  the<br>
                         company  played  a  leading  role  in  the  formation  of  the  Montgomery  County  Fireman’s  Association.<br>
                         
                         
                         Rejuvenation—<br><br>
                         
                         Following  a  period  of  inactivity  that  began  in  1914,  the  company  was  reorganized  on  November  16,  1922. <br>
                         Members  at  that  time  included  original  charter  members  Morris  Horner,  Horace  Ervien  and  Carl  Luckow.<br>
                         Mr.  Ervien  was  elected  president  pro-tem  along  with  Samuel  Johnston,  secretary-treasurer  pro-tem.  This  <br>
                         meeting  was  convened  to  call  the  company  back  to  active  duty  as  a  firefighting  unit  and  to  fill  the  offices<br>
                         vacated  by  the  deaths  of  Mr.  Engle  and  Mr.  Clayton,  who  had  held  office  continuously  since  1892.<br><br>
                         
                         A  few  days  later  the  members  elected  a  complete  slate  of  permanent  officers.  The  new  president  was<br>
                         James  O’Neil,  a  builder  and  house  mover  by  trade.  He  served  in  this  office  until  1930.  The  treasurer  was<br>
                         Carl  Luckow,  who  served  until  his  death  in  1946.  The  secretary  was  Samuel  Johnston,  who  served  in  office<br>
                         until  1939.  The  new  chief  was  J.  Harvey  Gottschalk,  owner  of  a  garage,  who  served  until  1924.  His  five<br>
                         sons  later  served  Ogontz  Fire  Company  in  many  capacities.<br><br>
                         
                         The  members  of  the  rejuvenated  company  discovered  that  they  had  inherited  about  $1,000  from  the<br>
                         original  members  and  decided  to  buy  their  first  motor  truck—a  right-hand  drive  simplex  chassis mounted<br>
                         with  an  American-LaFrance  pump.  This  truck  carried  two-50  gallon  tanks,  500  feet  of  hose  and  two  ladders.<br>
                         The  active  crew  consisted  of  23  men.<br><br>
                         
                         Eugene  Hawlk  loaned  the  company  a  building  at  8036  Old  York  Road  for  meetings  and  social  events<br>
                         beginning  in  September  of  1923.  The  truck  was  stored  in  the  township  garage.<br><br>
                         
                         First  Home—<br><br>
                         
                         In  February  of  1925  the  company  moved  into  its  own  home  at  8100  Old  York  Road  on  land  leased  from  the<br>
                         Reading  Company.  This  was  the  first  time  the  company  could  carry  out  all  its  operations  under  one  roof;  it<br>
                         remained  the  company’s  home  for  the  next  28  years.  In  the  fall  of  1926  the  Ogontz  Fire  Company  put  an<br>
                         American-LaFrance  pumper  at  a  cost  of  $6,300  into  service.  In  March  1929,  the  company  replaced  the  old<br>
                         Simplex  with  another  American-LaFrance  truck  at  a  cost  of  $5,600.  This  truck  served  the  company  for  10  <br>
                         years.  <br><br>
                         
                         William  N.  Gottschalk,  son  of  the  late  chief  J.  Harvey  Gottschalk,  served  as  chief  in  1932  and  again  in<br>
                         1937. Gottschalk  was  an  employee  of  the  Cheltenham  Township  Highway  Department  and  later  became<br>
                         building  inspector  and  fire  marshal.  John  Norris  was  chief  in  1938.  Henry  Kammere  served  as  chief  from<br>
                         1933  to  1935.  S.  Clarence  Johnston  was  chief  in  1936,  but  was  force  to  resign  to  attend  to  his<br>
                         responsibilities  on  the  police  force.<br><br>
                         
                         In  1947,  William  H.  Adair,  Sr.,  became  president,  and  was  succeeded  by  James  Goodwin  a  year  later.  At<br>
                         age  23,  John  Steinmetz  became  the  youngest  chief  of  Ogontz  Fire  Company,  a  position  he  retained  for  two  <br>
                         years.<br><br>
                         
                         Second  Home—<br><br>

                         During  this  time  the  members  began  to  draw  up  plans  for  a  new  building.  A  lot  at  Church  and  Old  York<br>
                         Roads  was  procured  for  $6,500.  In  1952  final  plans  were  approved  and  ground  breaking  took  place  that<br>
                         September.  In  March  1953,  the  building  that  had  been  home  to  Ogontz  Fire  Company  since  1925  was  sold<br>
                         to  the  Fleck  Company.  <br><br>

                         On  Saturday,  May  16,  1953,  a  formal  ceremony  and  parade  marked  the  official  opening  of  the  new<br>
                         firehouse.  Township  commissioners  and  the  Hon.  William  F.  Dannehower,  Judge  of  the  court  of  common<br>
                         pleas,  was  the  main  speaker.  Township  manager  Harold  C.  Pike  presented  President  Gottschalk  with  an<br>
                         oversize  key  to  mark  the  occasion.  On  that  date,  the  company’s  Mack  “85”  pumper  truck  along  with  a  new<br>
                         Mack  “75”  booster  truck  were  formally  housed  in  the  new  facility <br><br>

                         Increasingly  sophisticated  equipment  was  added  during  the  1950,  including  self-contained  breathing<br>
                         apparatus  and  foam  generating  nozzles.  In  1952  the  company  added  FM  radios.  They  helped  found  the<br>
                         Montgomery  Fire  Communication  Association.  Many  of  their  innovations  remain  in  use  today.<br><br>

                         By  1959  the  crew  realized  that  their  trucks  were  insufficient  to  carry  new  and  specialized  equipment. The<br>
                         company  purchased  a  GMC  truck  chassis  and  had  a  body  mounted  that  could  accommodate  additional<br>
                         equipment  in  addition  to  personnel.  The  truck  was  called  a  “squad  truck”  and  proved  to  be  such  an  asset<br>
                         that  other  fire  companies  in  the  region  copied  the  design.   <br><br>

                         On  April  14,  1960,  Samuel  Johnston,  whose  life  had  been  entwined  with  that  of  the  company  since  1910,<br>
                         passed  away.  The  company  dedicated  a  plaque  on  the  front  of  the  firehouse  to  his  memory.  It  included  a<br>
                         quotation  taken  from  Mr.  Johnston,  who  wrote  at  the  time  of  the  company’s  re-formation  “May  it  never  go<br>
                         asleep  again."<br><br>

                         Ogontz  took  a  major  step  forward  in  modernizing  its  equipment  in  the  ‘60s.  A  home  alert  system  was<br>
                         installed  in  the  households  of  crewmembers  to  supplement  the  building’s  siren.  For  the  first  time,  diesel<br>
                         powered  pumpers  were  ordered.  These  new  matching  Mack  C-95  engines  were  white  in  color—to  facilitate<br>
                         visibility  in  traffic—and  introduced  the  image  depicting  our  namesake,  Native  American  Chief  Ogontz.<br><br>

                         In  the  1970s,  Ogontz  augmented  its  mission,  becoming  Cheltenham  Township’s  fire  and  rescue  company.<br>
                         To  meet  its  new  responsibilities,  a  1971  Mack  MB  chassis  with  Swab  walk-in  body  was  designed  as  a<br>
                         heavy  rescue  truck,  replacing  the  1959  squad/rescue  vehicle.  Two  sets  of  Hurst  “jaws  of  life”  extrication<br>
                         tools  were  carried  on  the  new  truck.  Since  that  time,  Ogontz  has  distinguished  itself  by  increasing  its  rescue<br>
                         capabilities.  In  addition  to  extensive  training  in  fighting  fires,  today’s  crewmembers  are  certified  in  an  array<br>
                         of  specialized  rescue  techniques,  including  high-angle  rescue,  confined  space  rescue,  water  rescue,  and<br>
                         ice  rescues  in  addition  to  a  primary  emphasis  on  vehicle  rescues.<br><br>

                         Current  Home—<br><br>

                         As  the  community  developed,  the  fire  company  began  a  search  for  a  more  efficient  base  of  operations.  In<br>
                         1980,  Ogontz  dedicated  a  new  multi-bay  station  at  the  intersection  of  Old  York  Road  and  the  York  Road<br>
                         Spur.  At  the  same  time,  new  twin  Mack  CF  1250  GPM  diesel  pumpers  replaced  the  1966  Mack  pumpers. <br><br>

                         To  help  defray  costs  associated  with  the  new  facility  and  equipment,  the  fire  company  embarked  on  what<br>
                         would  become  a  community  tradition  for  the  next  35  years—the  annual  Christmas  tree  sale. <br><br>

                         The  fire  company  continued  to  grow  and  prosper  throughout  the  1980s,  ‘90s,  and  into  the  21st  Century.  The<br>
                         company  saw  an  increase  in  membership  and  introduced  innovative  firefighting  and  rescue  equipment<br>
                         under  the  leadership  of  Chief  Charlie  Farrington,  whose  record-breaking  tenure  lasted  from  1981  to  2007.   <br><br>

                         A  significant  fire,  said  to  be  the  largest  ever  to  strike  “downtown  Ogontz”  began  on  the  evening  of  March<br>
                         20,  1989.  The  Yorktown  Inn  was  reported  on  fire  at  5:39  p.m.  Firefighters  from  Ogontz,  Elkins  Park  and<br>
                         LaMott  met  heavy  flames  upon  arrival,  and  summoned  assistance  from  surrounding  fire  companies.  While<br>
                         the  flames  destroyed  the  100-year-old  building,  the  semi-attached  Yorktown  Mall  of  Shops  was  saved.<br>
                         Firefighters  remained  on  duty  until  11:20  the  following  morning—a  record  total  of  17  hours,  41  minutes  on<br>
                         scene.  <br><br>

                         A  mobile  air  compressor  unit,  enabling  on-site  refill  of  breathing  containers,  was  added  as  the  fourth  piece<br>
                         of  fire  apparatus  in  1990.  <br><br>

                         On  April  16  1994,  at  a  house  fire  on  Twickenham  Road  in  Glenside,  Ogontz  firefighter  Ed  Knox  sustained  a<br>
                         life-threatening  injury  and  was  rescued  by  fellow  firefighters  from  Edge  Hill  and  Ogontz  Fire  Companies.<br>
                         Ogontz  colleagues  Rich  Echeverria,  John  Preston,  and  Mark  Salvatore  performed  first  aid  and  CPR.  Ed<br>
                         survived  that  event  and  continued  to  serve  Ogontz  for  20  more  years.  He  celebrated  April  16th  as  his<br>
                         “second  birthday”  for  the  rest  of  his  life. <br><br>

                         The  21st  Century  has  significantly  increased  the  demands  placed  on  the  crewmembers  of  Ogontz  and  all<br>
                         volunteer  fire  companies  across  the  country.  The  number  of  incidents  we  respond  to  has  increased<br>
                         steadily,  even  as  the  number  of  people  willing  to  serve  the  volunteer  service  has  dropped.  From  an  average<br>
                         of  170  calls  a  year  in  the  early  1990s,  we  now  average  more  than  400  call  per  year.  A  record-smashing<br>
                         week  occurred  in  2012,  when  we  responded  to  more  than  50  events  within  a  four-day  period  following<br>
                         Hurricane  Sandy.  <br> <br>

                         Several  factors  contribute  to  our  increasing  workload,  including:  expanded  responsibilities  for  handling  non-<br>
                         fire  related  events  (for  example:  electrical  hazards,  dangerous  gases,  elevator  rescues,  floods,  downed<br>
                         trees,  and  other  problems).  Increased  reliance  on  automatic  alarm  systems  raises  the  incidence  of  false<br>
                         alarms  requiring  our  attention,  and  the  decline  in  numbers  of  volunteers  willing  to  take  on  these<br>
                         responsibilities  means  we  are  frequently  called  to  events  outside  our  district. <br><br>

                         Our  training  requirements  have  increased  due  to  the  sophisticated  equipment  and  techniques  we  now  use.<br>
                         Additionally,  federal,  state  and  local  certification  programs  have  been  mandated  to  address  threats  of  the<br>
                         kind  initiated  during  attacks  of  September  11,  2001  and  potential  threats  identified  since  then. <br><br>

                        The  overall  impact  is  increased  demands  on  our  time  for  training  and  responding  to  emergencies,  yet  fewer<br>
                        people  to  share  the  workload  with  us.  Nevertheless,  we,  the  members  of  Ogontz  Fire  Company,  remain  as<br>
                        committed  today  as  we  and  our  predecessors  have  been  for  125  years  to  protecting  the  lives  and  property<br>
                        of  the  people  of  Cheltenham  Township.  Our  service  is  an  awesome  responsibility,  a  high  honor,  and  a<br>
                        privilege.  Indeed,  we  do  “face  the  past  with  pride  and  the  future  with  courage.” <br><br>

